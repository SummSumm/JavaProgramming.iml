package day18_NestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {

        String w = "Wooden Spoon ";
        for (int j = 0; j < 2; j++) {
            for (int i = 0; i < 3; i++) {
                System.out.println(w);  // içiçe olanda birbiri kadar yapar. Her iç loop dıştaki kadar tekrarlanır

        }
        }}
    }

