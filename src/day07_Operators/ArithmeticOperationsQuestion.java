package day07_Operators;

public class ArithmeticOperationsQuestion {
    public static void main(String[] args) {
        // Print "10 divided by 3 equals 3 with a remainder of 1."

        int num1 = 10;
        int num2 = 3;

        int division = 10/3;
        int remainder = 10%3;

        System.out.println(num1 + " divided by " + num2 + " equals " + division + " with a remainder of " + remainder + ".");

    }
}
